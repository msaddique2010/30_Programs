// 11. Write a program to declare and initialize two variables of "int" type. Calculate the average of these values and print the result on screen. 

public class Program11_java {
    public void Program11(){
        System.out.println("11.	Write a program to declare and initialize two variables of \"int\" type. Calculate the average of these values and print the result on screen.\n ");

        // Processing
        int num1 = 10;
        int num2 = 20;
        double average = (num1 + num2) / 2.0;

        // Output
        System.out.println("The average of " + num1 + " and " + num2 + " is: " + average);
    }
}
