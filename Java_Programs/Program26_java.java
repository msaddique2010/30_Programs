// 26.	Write a program to convert 12000 rupees into dollars (1 dollar = Rs. 60) 


public class Program26_java {
    public void Program26() {
        System.out.println("26.\tWrite a program to convert 12000 rupees into dollars (1 dollar = Rs. 60) \n");
        // Input
        double Rupees, Dollar;
        Rupees = 12000;

        // Processing
        Dollar = Rupees / 60;

        // Output
        System.out.println("Dollar = " + Dollar); 
    }   
}
