public class Program10_java {
    public void Program10(){
        System.out.println("10. Write a program to convert 2.5 miles into kilometers and print the result on screen. (Hint: 1 mile = 1.609 kilometers) \n");
        // Processing
        double miles = 2.5;

        double km = miles * 1.609;

        // Output
        System.out.println("Distance in kilometers: " + km);
    }
}
